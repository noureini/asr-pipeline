BN_DIGIT = {"০":"0","১":"1","২":"2","৩":"3","৪":"4",
            "৫":"5","৬":"6","৭":"7","৮":"8","৯":"9"}
# Spelled Bengali number words appearing in this set -> digits
WORD_NUM = {"একশ":"100", "একশো":"100", "এক হাজার":"1000", "একহাজার":"1000"}

def base_norm(s):
    s = unicodedata.normalize("NFC", s).strip()
    s = re.sub(r"[।,.!?‌‍]", "", s)        # danda, latin punct, ZWNJ/ZWJ
    s = re.sub(r"\s+", " ", s)
    return s

def num_norm(s):
    for w, d in WORD_NUM.items():
        s = s.replace(w, d)
    for bn, en in BN_DIGIT.items():
        s = s.replace(bn, en)
    return re.sub(r"\s+", " ", s).strip()

def cer(ref, hyp):
    r, h = list(ref), list(hyp)
    n, m = len(r), len(h)
    d = list(range(m + 1))
    for i in range(1, n + 1):
        prev = d[0]; d[0] = i
        for j in range(1, m + 1):
            cur = d[j]
            cost = 0 if r[i-1] == h[j-1] else 1
            d[j] = min(d[j] + 1, d[j-1] + 1, prev + cost)
            prev = cur
    return d[m] / max(n, 1)

def run(label, normfn):
    print(f"\n=== {label} ===")
    print(f"{'file':>9} {'ASR':>8} {'ASR+Omni':>9}  winner")
    sa = so = 0.0
    for f in sorted(REF):
        r = normfn(base_norm(REF[f]))
        a = normfn(base_norm(ASR[f]))
        o = normfn(base_norm(OMNI[f]))
        ca, co = cer(r, a) * 100, cer(r, o) * 100
        sa += ca; so += co
        w = "Omni" if co < ca - 1e-9 else ("ASR" if ca < co - 1e-9 else "tie")
        print(f"{f:>9} {ca:7.1f}% {co:8.1f}%  {w}")
    n = len(REF)
    print(f"{'MEAN':>9} {sa/n:7.1f}% {so/n:8.1f}%   "
          f"rel.reduction {100*(sa-so)/sa:5.1f}%")

run("M1  NFC + punctuation/ZW stripped", lambda s: s)
run("M2  M1 + number normalization",      num_norm)
